package com.hp.ts.rnd.tool.perf.threads.rest;

class WLSEntry {
	private String wlsUrl;

	private String wlsUser;

	private String wlsPassword;

	private String wlsName;

	private String agentId;

	public String getWlsUrl() {
		return wlsUrl;
	}

	public void setWlsUrl(String wlsUrl) {
		this.wlsUrl = wlsUrl;
	}

	public String getWlsUser() {
		return wlsUser;
	}

	public void setWlsUser(String wlsUser) {
		this.wlsUser = wlsUser;
	}

	public String getWlsName() {
		return wlsName;
	}

	public void setWlsName(String wlsName) {
		this.wlsName = wlsName;
	}

	public String getWlsPassword() {
		return wlsPassword;
	}

	public void setWlsPassword(String wlsPassword) {
		this.wlsPassword = wlsPassword;
	}

	public String getAgentId() {
		return agentId;
	}

	public void setAgentId(String agentId) {
		this.agentId = agentId;
	}

}
